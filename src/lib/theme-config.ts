// Utility functions for the new theme system
export function getCompleteTheme(themeName: ThemeName): CompleteTheme | undefined {
  return completeThemes.find(theme => theme.value === themeName)
}

export function getDefaultTheme(): CompleteTheme {
  return getCompleteTheme(defaultThemeName) || completeThemes[0]
}

// Apply complete theme function
export function applyCompleteTheme(theme: CompleteTheme, isDark: boolean = false, fontOverride?: string | null) {
  const root = document.documentElement
  const colors = isDark ? theme.darkColors : theme.lightColors
  
  // Apply all CSS variables
  Object.entries(colors).forEach(([key, value]) => {
    root.style.setProperty(key, value)
  })
  
  // Apply border radius
  root.style.setProperty('--radius', theme.borderRadius)
  
  // Determine which font to use
  const fontToUse = fontOverride || theme.fontFamily
  const fontConfig = fontOverride ? availableFonts.find(f => f.value === fontOverride) : null
  const fontWeight = fontConfig?.fontWeight || theme.fontWeight
  const googleFontUrl = fontConfig?.googleFontUrl || theme.googleFontUrl
  
  // Apply font family
  root.style.setProperty('--font-family', fontToUse)
  
  // Load Google Font if needed
  if (googleFontUrl) {
    loadGoogleFont(googleFontUrl)
  }
  
  // Apply font to body
  document.body.style.fontFamily = fontToUse
  document.body.style.fontWeight = fontWeight
}

// Google Font loader
function loadGoogleFont(url: string) {
  // Check if font is already loaded
  const existingLink = document.querySelector(`link[href="${url}"]`)
  if (existingLink) return
  
  // Create and append link element
  const link = document.createElement('link')
  link.rel = 'stylesheet'
  link.href = url
  link.crossOrigin = 'anonymous'
  document.head.appendChild(link)
}